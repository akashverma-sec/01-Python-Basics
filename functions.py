# Simple Function

def greet():
    print("Hello Akash!")
    print("Welcome to Python Functions.")

greet()

# Function with Arguments

def greet(name):
    print("Hello", name)
    print("Welcome to Python!")

greet("Akash")
greet("Rahul")

# Function with Return

def add(a, b):
    return a + b

result = add(10, 20)

print("Addition =", result)