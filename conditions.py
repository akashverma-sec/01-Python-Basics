# if statement

age = 20

if age >= 18:
    print("You are eligible to vote.")

# if-else statement

age = 13 
if age >= 18:
    print("You are eligible to vote.")
else:
    print("You are not eligible to vote.")


# if-elif-else Statement

marks = 55

if marks >= 90:
    print("Grade A")
elif marks >= 75:
    print("Grade B")
elif marks >= 60:
    print("Grade C")
else:
    print("Fail")