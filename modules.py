# Math module 
import math

print("Square Root:", math.sqrt(25))
print("Power:", math.pow(2, 5))
print("Pi Value:", math.pi)

# Random module
import random

print("Random Number:", random.randint(1, 100))

fruits = ["Apple", "Banana", "Mango", "Orange"]

print("Random Fruit:", random.choice(fruits))

